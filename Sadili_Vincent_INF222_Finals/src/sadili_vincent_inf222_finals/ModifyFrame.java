    private void workerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_workerTableMouseClicked
        // TODO add your handling code here:
        int row = workerTable.getSelectedRow();
      
        String un = workerTable.getValueAt(row, 2) + "";
        String pw = workerTable.getValueAt(row, 3) + "";
        String stat = workerTable.getValueAt(row, 7) + "";
        String job = workerTable.getValueAt(row, 8) + "";
        jTextField1.setText(un);
        jTextField2.setText(pw);
        
        if(stat.equalsIgnoreCase("Deactivated")){
            jToggleButton1.setSelected(true);
        }else if(stat.equalsIgnoreCase("activated")){
           jToggleButton1.setSelected(false); 
        }
        
        if(job.equalsIgnoreCase("Super Admin")){
            jComboBox1.setSelectedItem("Super Admin");
        }else if(job.equalsIgnoreCase("Admin")){
            jComboBox1.setSelectedItem("Admin");
        }else if(job.equalsIgnoreCase("Cashier")){
            jComboBox1.setSelectedItem("Cashier");
        }
       
    }//GEN-LAST:event_workerTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = workerTable.getSelectedRow();
        String data2 = jTextField1.getText();
        String data3 = jTextField2.getText();
        String stat="";
        String job=jComboBox1.getSelectedItem().toString();
        if(jToggleButton1.isSelected()){
            stat="ACTIVATED";
            jToggleButton1.setText("Deactivate");
        }else{
            stat="DEACTIVATED";
            jToggleButton1.setText("Activate");
        }
        
        
        
        workerTable.setValueAt(data2, row, 2);
        workerTable.setValueAt(data3, row, 3);
        workerTable.setValueAt(stat, row, 7);
        workerTable.setValueAt(job, row, 8);
        UserAccount.username.set(row, data2);
        UserAccount.password.set(row, data3);
        UserAccount.accountStat.set(row, stat);
        UserAccount.job.set(row, job);
        JOptionPane.showMessageDialog(null, "ACCOUNT UPDATED!");
    }//GEN-LAST:event_jButton1ActionPerformed

