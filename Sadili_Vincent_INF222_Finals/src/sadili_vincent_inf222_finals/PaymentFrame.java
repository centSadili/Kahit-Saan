    private static final int MAX_NUMBER = 99999;  // Maximum value for each number segment
    private static final int SEGMENT_COUNT = 3;   // Number of segments in the output
    
    public static String generateUniqueNumber() {
        StringBuilder sb = new StringBuilder();
        
        for (int i = 0; i < SEGMENT_COUNT; i++) {
            int segment = generateUniqueSegment();
            sb.append(String.format("%05d", segment));  // Pad the segment with leading zeros
            if (i < SEGMENT_COUNT - 1) {
                sb.append("-");
            }
        }
        
        return sb.toString();
    }
    
    private static int generateUniqueSegment() {
        Set<Integer> generatedNumbers = new HashSet<>();
        Random random = new Random();
        
        while (true) {
            int segment = random.nextInt(MAX_NUMBER + 1);
            if (!generatedNumbers.contains(segment)) {
                generatedNumbers.add(segment);
                return segment;
            }
        }
    }
