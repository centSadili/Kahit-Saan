    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = workerTable.getSelectedRow();
        int reply = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?");
        if(reply == JOptionPane.YES_OPTION)
        {
            ((DefaultTableModel) workerTable.getModel()).removeRow(workerTable.getSelectedRow());
            UserAccount.Fullname.remove(row);
            UserAccount.username.remove(row);
            UserAccount.password.remove(row);
            UserAccount.birthday.remove(row);
            UserAccount.age.remove(row);
            UserAccount.gender.remove(row);
            UserAccount.accountStat.remove(row);
            UserAccount.job.remove(row);
        
            JOptionPane.showMessageDialog(null, "ACCOUNT DELETED!");
           
        }
    }//GEN-LAST:event_jButton2ActionPerformed

