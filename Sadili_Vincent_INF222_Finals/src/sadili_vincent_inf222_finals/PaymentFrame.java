    private void pModejComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_pModejComboBoxItemStateChanged
        // TODO add your handling code here:
         String mode=pModejComboBox.getSelectedItem().toString();
        if(mode.equalsIgnoreCase("cash")){
            this.setTrans("Cash");
            receiveButton.setEnabled(false);
            amountTextField.setEditable(true);
            amountTextField.setEnabled(true);
            payButton.setEnabled(true);
        }else{
            if(mode.equalsIgnoreCase("Gcash")){
             this.setTrans("Gcash");   
            }else if(mode.equalsIgnoreCase("Bank")){
              this.setTrans("Bank");   
            }
            receiveButton.setEnabled(true);
            amountTextField.setEditable(false);
            amountTextField.setEnabled(false);
            payButton.setEnabled(false);
        }
    }//GEN-LAST:event_pModejComboBoxItemStateChanged

